package sayan.java;
public class Flodoub1 {
    public static void main(String[] args) {
        int myInt=5/3;
        float myFloat=5f/3f;
        double myDoub=5d/3d;
        System.out.println("Final int value:"+myInt);
        System.out.println("Final float value:"+myFloat);
        System.out.println("Final double value:"+myDoub);
    }
}

//Output--->

//Final int value:1
//Final float value:1.6666666
//Final double value:1.6666666666666667
